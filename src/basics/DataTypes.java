package basics;

public class DataTypes {
    public static void main(String[] args) {
//        Java is statically typed Language
        byte a = 127;
        short b = 2020;
        int c = 100312310;
        long d = 123011231231l;

        char e = 'A';
        boolean f = true;
        float g = 3.14f;
        double h = 3.14;

        boolean ee = true ^ false;
    }
}
